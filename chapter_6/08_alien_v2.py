# Созадаем три словаря
alien_0 = {'color': 'green', 'points': 5}
alien_1 = {'color': 'yellow', 'points': 10}
alien_2 = {'color': 'red', 'points': 15}
# Помещаем словари в список
aliens = [alien_0, alien_1, alien_2]
# Перебираем список и выводим результат
for alien in aliens:
    print(alien)
print("\n")
