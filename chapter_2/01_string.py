"""Работа с строками"""
# Сохраняем строку в переменную
word = "Валя знает Python"
# Выводим строку и делаем заглавные буквы в каждом слове.
print(word.title())

word = 'Ольга ХОРОШО гоТОВИТ'
# Выводим строку и приводим все символы к нижнему регистру.
print(word.lower())

word = 'Нина Знает ФОТОшоп'
# Выводим строку и приводим все символы к верхнему регистру.
print(word.upper())