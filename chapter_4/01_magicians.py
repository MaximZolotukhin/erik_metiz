# Перебор всего списка с помощью цикла for
magicians = ['гарри поттер', 'рон визли', 'гермиона грейнжер']
for magician in magicians:
    print(f'{magician.title()} - это был великий трюк')
    print(f'Я не могу дождаться, чтобы увидеть следующий трюк {magician.title()}.\n')

print(f'Спасибо всем. Это было великолепное магическое представление.')
